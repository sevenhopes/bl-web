				<dt class="faq-admission top-10"><strong>[입학/퇴원]</strong> 입학 절차는 어떻게 되나요?</dt>
				<dd>전화로 <strong>상담 예약</strong>을 하시고, 예약 날짜와 시간에 맞춰 오시면 학생진단과 학부모상담을 받으실 수 있습니다. 그리고 입학원서를 작성하고 수강료와 교재비를 납부하시면 입학 절차와 완료 됩니다.</dd>
				<dt class="faq-admission"><strong>[입학/퇴원]</strong> 방문, 상담 시 꼭 예약 해야 하나요?</dt>
				<dd>예, 상담은 예약제로 운영 됩니다. 간혹 예약 없이 들르셨다가 상담일정이 꽉 차서 오래 기다리시거나, 일정상 그냥 돌아가시는 경우가 있을 수 있으니 꼭 예약을 하고 방문해 주시기 바랍니다. 참고로, 학원 오픈시간은 <strong>오후 2시</strong>입니다.</dd>
				<dt class="faq-admission"><strong>[입학/퇴원]</strong> 학기 중간에도 입학이 가능한가요?</dt>
				<dd>네, 등록 즉시 수업 참여가 가능합니다. 다만 학부모님과 상담 시 남은 학기와 진도, 신입생의 학년에 따라 당장 등록하는 것보다 다음 학기부터의 입학이 권장 되는 경우도 있습니다.</dd>
				<dt class="faq-admission"><strong>[입학/퇴원]</strong> 학년과 입학 레벨이 관계가 있나요?</dt>
				<dd>입학 레벨은 입학상담 시 진단된 결과에 따라 신입생의 학업성취도를 최대화 할 수 있도록 결정 됩니다. 즉, 학년과 레벨 배정에 직접적인 관계는 없습니다. 다만 비슷한 나이대의 학급을 선호하시기에 수업 시간과는 연관이 있습니다.</dd>
				<dt class="faq-admission"><strong>[입학/퇴원]</strong> 레벨과 시간의 배정은 어떻게 이루어지나요?</dt>
				<dd>상담 예약 후 방문하시어 입학상담 절차를 거쳐서 담당자가 신입생의 현재 능력에서 최대의 이해와 실력 향상을 얻을 수 있는 레벨로 배정이 됩니다. 수업 시간대는 비슷한 나이대의 학급을 유지하고자 학생의 나이에 따라 달라질 수 있는데, 보통 어린 원생들이 4:30~6:00 수업에, 나이가 있는 원생들이 7:30~9:00 수업에 배정을 원하십니다.</dd>
				<dt class="faq-admission top-10"><strong>[입학/퇴원]</strong> 상담은 어떻게 이루어지나요? 또 무료인가요?</dt>
				<dd>입학상담은 <strong>무료</strong>입니다. 상담 절차는 1-예약, 2-방문, 3-영어인터뷰, 4-레벨테스트, 5-학부모상담 순으로 총 20~30분 정도 이루어집니다. 이후 입학을 원하시면 6-입학원서 작성, 7-수강료와 교재비 결제, 8-수업 참여 순으로 진행 됩니다.</dd>
				<dt class="faq-admission"><strong>[입학/퇴원]</strong> 입학 상담을 자녀가 꼭 같이 가야 하나요?</dt>
				<dd>정확한 상담을 위해 자녀와 꼭 같이 오시는 것을 권장합니다. 학부모님과의 상담만으로는 커리큘럼, 반배정 등과 같은 중요한 주제에 있어서 원활한 진행이 어렵기 때문입니다. 또한 첫등원 전에 학원에 한 번 들러보는 것도 때론 학생에게 큰 도움이 됩니다.</dd>

				<dt class="faq-level"><strong>[레벨/학기]</strong> 정규학기 레벨은 어떻게 구성 되어 있나요?</dt>
				<dd>정규학기 레벨은 크게 <strong>Basic, Special, Stage</strong> 세 가지 단계로 나뉘며, 총 11 단계로 이루어져 있습니다. (Basic 1~6, Special, Stage I, D, A, M) 프로그램과 관련한 자세한 사항은 <strong>{<a href="/curriculum/course-overview/">Curriculum</a>}</strong>의 페이지들을 참고해 주시기 바랍니다.</dd>
				<dt class="faq-level"><strong>[레벨/학기]</strong> 한 학기는 몇 개월인가요?</dt>
				<dd>한 학기는 <strong>6개월</strong>이며, 봄-여름학기(3~8월), 가을-겨울학기(9~2월)로 구성 됩니다.</dd>
				<dt class="faq-level"><strong>[레벨/학기]</strong> 다음 레벨로의 이동은 어떻게 이루어지나요?</dt>
				<dd>학기가 끝나면 중간고사, 기말고사, Speaking Test 등 종합적 학업성취도 결과에 따라 다음 레벨이 결정 됩니다. 수업에 꾸준히 참여하는 경우 대부분의 원생들이 다음 레벨로 큰 이슈 없이 올라가게 되지만, 여러 가지 사정상 그렇지 못하는 경우도 있습니다.</dd>
				<dt class="faq-level"><strong>[레벨/학기]</strong> 동일 레벨을 반복 수강하는 경우, 교재를 다시 사야 하나요?</dt>
				<dd>보통 교재는 바뀌지 않습니다만, 필기 등 교재를 직접 사용한 경우 새 책을 위한 비용이 부분적으로 청구될 수는 있습니다.</dd>

				<dt class="faq-class"><strong>[수업/교실]</strong> 학생 관리는 어떻게 이루어지나요?</dt>
				<dd>등록 후 반 배정이 되면 담임선생님의 관리 하에 어학원 생활이 이루어집니다. 중간고사, 기말고사 이후 담임선생님과의 전화상담이 정기적으로 있으며, 필요한 경우 학기 중 언제라도 원장단이나 담임선생님과 상담을 진행할 수 있습니다.</dd>
				<dt class="faq-class"><strong>[수업/교실]</strong> 상담을 하고 싶은 경우 어떻게 하나요?</dt>
				<dd><strong>수업</strong>에 관련한 경우, 1차적으로 <strong>담임선생님</strong>에게 연락 주시면 됩니다. <strong>교실수업의 이외</strong>의 내용에 대해서는 어학원 <strong>대표전화</strong>로 연락 주시면 상담 일정을 잡을 수 있으며, <strong>통학버스</strong>에 관한 경우 이용하는 <strong>버스 기사님</strong>이나 <strong>도우미 선생님</strong>과 통화하시면 됩니다.</dd>
				<dt class="faq-class"><strong>[수업/교실]</strong> 학기 중에 수업의 시간대 이동이 가능한가요?</dt>
				<dd>네, 이동을 원하시는 시간대의 정원이 <strong>만원이 아닌 경우</strong> 시간대 이동이 가능합니다. 부득이한 경우 담임선생님과 상담하여 요일별로 원하는 시간대에 수업에 참여할 수도 있습니다. (예, 월수금: 6시, 화목: 7시반)</dd>
				<dd></dd>
				<dt class="faq-class top-10"><strong>[수업/교실]</strong> 수업 일수와 시간은 어떤가요?</dt>
				<dd>수업은 공휴일이 없으면 기본적으로 월~금 <strong>주5일</strong>입니다. 수업 시간은 1일 <strong>1시간 20분</strong>으로 시간대는 오후 4:30~5:50, 6:00~7:20, 7:30~8:50입니다.</dd>
				<dt class="faq-class top-10"><strong>[수업/교실]</strong> 원어민 선생님 수업이 있나요?</dt>
				<dd>선생님의 국적에 관계없이 브릿지라잇의 수업은 <strong>모두 영어로 진행</strong> 됩니다. 또한 모든 레벨에 원어민 선생님과의 수업이 배정되어 있습니다. 1주 5일 중 원어민 선생님 수업은 다음과 같습니다.<br>
					<ul>
						<li>Basic: 내국인 선생님 4일 + 원어민 선생님 함께 1일</li>
						<!-- <li>Special: 내국인 선생님 & 원어민 선생님 함께 5일</li> -->
						<li>Stage: SI, SD, SA: 내국인 선생님 3일 + 원어민 선생님 2일 / SM: 내국인 선생님 2일 + 원어민 선생님 3일</li>
					</ul>
				</dd>
				<dt class="faq-class top-10"><strong>[수업/교실]</strong> 수업 시작시간과 끝나는 시간은 언제인가요?</dt>
				<dd>저희 수업 시간은 다음과 같습니다. 레벨에 따라 이 중 하루 1회나 2회 수업하는 경우도 있으므로 참고하시기 바랍니다.<br>
					<ul>
						<li>첫번째 수업 시간대: 오후 4:20~5:50</li>
						<li>두번째 수업 시간대: 오후 6:00~7:20</li>
						<li>세번째 수업 시간대: 오후 7:30~8:50</li>
					</ul>
				</dd>
				<dt class="faq-class top-10"><strong>[수업/교실]</strong> 학기 첫날이나 등원 첫날 어디로 가야 하나요?</dt>
				<dd>
					<ul>
						<li><strong>신입생</strong>: 등원 첫날에는 상담을 했던 <strong>지하1층</strong> 로비로 내려오시면 됩니다. 그러면 안내선생님이나 담임선생님이 수업 시작 전에 교실로 안내해 줍니다.</li>
				  	<li><strong>재학생</strong>: 학기 첫날에는 자기가 지난 학기(며칠 전까지) <strong>다니던 교실</strong>로 등원하면 됩니다. 그러면 수업 시작 후 담임선생님이 교우들과 같이 다음 교실로 이동시켜 줍니다.</li>
				  </ul>
			  </dd>
				<dt class="faq-class top-10"><strong>[수업/교실]</strong> 방학 중 영어캠프 시간과 수업 내용은 어떤가요?</dt>
				<dd>여름/겨울 방학 영어캠프의 수업시간은 <strong>오전 9:00~11:50</strong>입니다. 수업의 종류는 크게 세 가지로, 저레벨을 위해 재밌게 접근하는 <strong>영어뮤지컬</strong>, 문법강화를 위한 <strong>문법캠프(초/중/고급)</strong>, 그리고 고레벨 논리스피킹을 위한 <strong>영어논쟁캠프</strong>입니다. 자세한 내용은 {<a href="/curriculum/summer-winter-camp/">여름/겨울 영어캠프</a>} 페이지를 참고하시기 바랍니다.</dd>
				<dt class="faq-class"><strong>[수업/교실]</strong> 방학 중 영어캠프 기간 중에 정규수업도 하나요?</dt>
				<dd>방학 중 영어캠프와 정규학기 수업은 시간대가 겹치지 않으며, 학기수업은 캠프기간 중에도 차질없이 진행됩니다. 따라서 캠프에 참가하면 2주 동안 하루 4시간반(오전 캠프 3시간, 오후 정규수업 1시간반)을 영어로 생활하는 것이기에 그만큼 실력 향상 비율이 평소보다 월등히 높습니다.</dd>

				<dt class="faq-tuition top-10"><strong>[수강료/교재비]</strong> 정규학기 수강료는 얼마인가요?</dt>
				<dd>정기학기 수업의 경우 수강료는 한 달에 27만원이며, 통학버스를 이용할 경우 1만원이 추가 됩니다. 학기 시작 시 6개월 분의 교재비가 별도로 있습니다.</dd>
				<dt class="faq-tuition"><strong>[수강료/교재비]</strong> 교재비는 얼마인가요?</dt>
				<dd>교재비는 레벨마다 차이가 있어서 단정지어 말씀드리기 어렵습니다만, 보통 Basic 레벨은 6~8만원, Stage 레벨은 4~5만원 정도입니다.</dd>
				<dt class="faq-tuition"><strong>[수강료/교재비]</strong> 수강료 납부일은 언제인가요?</dt>
				<dd>수강료 납부일은 <strong>매월 1일</strong>로, 해당 월의 수업을 위한 수강료를 선납하는 방식입니다. 예를 들면, 7월분 수강료는 7월 1일에 납부하게 됩니다.</dd>
				<dt class="faq-tuition"><strong>[수강료/교재비]</strong> 수강료 할인 같은 건 없나요?</dt>
				<dd>브릿지라잇 어학원은 타 학원에서는 찾기 힘든 세 종류의 수강료 할인 혜택을 제공하고 있습니다. 다만, 다음 사항은 여러 가지 사유로 예고 없이 변경될 수 있으며, 각 혜택은 타인에게 양도될 수 없습니다.<br>
					<ul>
						<li><strong>여름/겨울 방학캠프 할인</strong>: 브릿지라잇 재학생은 캠프 참가비에서 2만원 할인</li>
						<li><strong>형제자매 할인</strong>: 정규학기에 한 가정으로부터 2명 이상의 자녀가 수강하는 경우, <strong>매월</strong> 수강료 5만원 할인 (자녀 2명: 매월 5만원, 자녀 3명 이상: 매월 10만원)</li>
						<li><strong>새학기 소개 할인</strong>: 새학기에 신입생을 추천해 주시는 부모님께 개강월(3월, 9월)의 수강료 5만원 할인 (소개해 주시는 분만 해당, 소개 받으시는 분 해당 아님)</li>
					</ul>
				<span class="comment">* 형제자매 할인과 새학기 소개 할인이 중복 해당 되는 경우, 최대 합산 할인은 10만원입니다.</span>
				</dd>
				<dt class="faq-tuition"><strong>[수강료/교재비]</strong> 방학 중 영어캠프나 미국현지체험연수의 수강료는 얼마인가요?</dt>
				<dd>영어캠프의 수강료이며 25만원이며 교재비 포함입니다. 미국현지체험연수의 경우 부득이하게 수강료가 매년 다르게 책정이 되기 때문에 별도의 상담이 필요합니다.</dd>
				<dt class="faq-tuition"><strong>[수강료/교재비]</strong> 공휴일로 인한 휴강 시 수강료의 차이가 있나요?</dt>
				<dd>공휴일로 인해 월 수업일수가 평월보다 1~2일 많거나 적더라도 수강료는 같습니다.</dd>
				<dt class="faq-tuition"><strong>[수강료/교재비]</strong> 수강료의 환불이 가능한가요?</dt>
				<dd>저희 어학원은 다음과 같이 법정 교습비 반환 규정을 따릅니다.<br>
